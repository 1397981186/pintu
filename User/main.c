#include "board.h"
#include "rtthread.h"
#include "./lcd/bsp_ili9341_lcd.h"
#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include "./lcd/bsp_xpt2046_lcd.h"
/*
*************************************************************************
*                               变量
*************************************************************************
*/
/* 定义线程控制块 */
static rt_thread_t test_thread = RT_NULL;

/*
*************************************************************************
*                             函数声明
*************************************************************************
*/
static void test_thread_entry(void* parameter);

/*
*************************************************************************
*                             main 函数
*************************************************************************
*/
/**
  * @brief  主函数
  * @param  无
  * @retval 无
  */
int main(void)
{	
    /* 
	 * 开发板硬件初始化，RTT系统初始化已经在main函数之前完成，
	 * 即在component.c文件中的rtthread_startup()函数中完成了。
	 * 所以在main函数中，只需要创建线程和启动线程即可。
	 */
	test_thread =                          /* 线程控制块指针 */
    rt_thread_create( "test",              /* 线程名字 */
                      test_thread_entry,   /* 线程入口函数 */
                      RT_NULL,             /* 线程入口函数参数 */
                      512,                 /* 线程栈大小 */
                      3,                   /* 线程的优先级 */
                      20);                 /* 线程时间片 */
                   
    /* 启动线程，开启调度 */
   if (test_thread != RT_NULL)
        rt_thread_startup(test_thread);
    else
        return -1;
		
}

/*
*************************************************************************
*                             线程定义
*************************************************************************
*/
/************************************************************
  * @brief   test_thread_entry
  * @param   NULL
  * @return  NULL
  * @author  fire
  * @github  
  * @date    2018-xx-xx
  * @version v1.0
	* @note    此处是dma发送实验，野火将其改写在rt_hw_console_output函数中
						 rt_kprintf是使用了串口dma打印。
  ***********************************************************/
static void test_thread_entry(void* parameter)
{
	while (1)
	{
		if (touchcount==0)
		{
			XPT2046_initialmap(map);
		}
	XPT2046_TouchEvenHandler();
	unsigned char * title1 ="拼";
	unsigned char * title2 ="图";
	unsigned char * title3 ="游";
	unsigned char * title4 ="戏";
	ILI9341_DisplayStringEx(20,20,40,40,title1,0);
	ILI9341_DisplayStringEx(20,60,40,40,title2,0);
	ILI9341_DisplayStringEx(20,100,40,40,title3,0);
	ILI9341_DisplayStringEx(20,140,40,40,title4,0);
	rt_thread_delay(10);
	}
}

